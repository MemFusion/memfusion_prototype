#pragma once


#include <queue>
#include <thread>
#include <mutex>
#include <condition_variable>


namespace MemFusion
{

template <typename T>
class syncqueue
{
public:
    T dequeue()
    {
        std::unique_lock<std::mutex> mlock(mutex_);
        while (queue_.empty())
        {
            cond_.wait(mlock);
        }
        auto item = queue_.front();
        queue_.pop();
        return (item);
    }

    void dequeue(T & item)
    {
        std::unique_lock<std::mutex> mlock(mutex_);
        while (queue_.empty())
        {
            cond_.wait(mlock);
        }
        item = queue_.front();
        queue_.pop();
    }

    size_t size()
    {
        std::unique_lock<std::mutex> mlock(mutex_);
        auto ret = queue_.size();
        mlock.unlock();
        cond_.notify_one();
        return (ret);
    }

    void enqueue(const T & item)
    {
        std::unique_lock<std::mutex> mlock(mutex_);
        queue_.push(item);
        mlock.unlock();
        cond_.notify_one();
    }

    void enqueue(T && item)
    {
        std::unique_lock<std::mutex> mlock(mutex_);
        queue_.push(std::move(item));
        mlock.unlock();
        cond_.notify_one();
    }

private:
    std::queue<T> queue_;
    std::mutex mutex_;
    std::condition_variable cond_;
};


}
